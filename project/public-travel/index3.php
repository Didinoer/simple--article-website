                    <h1 class="judul">
                    Paham Traveling Telah jadi Kebutuhan Masyarakat, Aplikasi Tiket.com Luncurkan Fitur Jaminan Harga Termurah                    
                    </h1>
                    <h3 class="penerbit">
                    Risna Halidi | 15 Juni 2022 12:07                    
                    </h3>
                    <img src="../img-travel/3.jpg" alt="abc" class="hero-img" width="600px">
                    <div class="isi-content">
                    <p>Aplikasi Online Travel Agent - Tiket.com, baru saja meluncurkan fitur cerdas terbaru bernama Jaminan Harga Termurah (JHT). Fitur tersebut dapat memberikan opsi dan pilihan harga sesuai kebutuhan perjalanan termurah di antara online travel platform lain.</p>

                    <p>Dikatakan VP Brand Marketing Tiket.com - Maria Risa Puspitasari, JHT merupakan salah satu wujud upaya keberhasilan pihaknya dalam mengidentifikasi pain point utama masyarakat dalam jalan-jalan, yaitu pengaturan dana.</p>

                    <p>"JHT menyederhanakan proses pencarian dan perbandingan harga tiket pesawat, hotel akomodasi, dan aktivitas di berbagai destinasi domestik, sehingga masyarakat dapat fokus dengan perencanaan perjalanan lainnya," kata Maria, Rabu (15/6/2022).</p>

                    <p>Fitur anyar Jaminan Harga Termurah berlaku untuk semua lini produk di bawah Tiket.com, mulai dari pesawat, hotel dan akomodasi, hingga segala keperluan dan kelengkapan aktivitas perjalanan di tiket To-Do (Atraksi, Event, Spa & Kecantikan, Playground, Olahraga dan Outdoor, serta Tes COVID-19).</p>

                    <p>Lebih lanjut, kata Maria, saat ini perjalanan telah menjadi kebutuhan primer banyak masyarakat Indonesia. Tetapi biaya perjalanan atau liburan sudah masuk ke dalam kategori budget mewah, sehingga keinginan berlibur bisa menambah beban pikiran karena harus menabung secara khusus.</p>

                    <p>Diluncurkan pada Rabu, (15/06), berikut adalah penjelasan mengenai fitur Jaminan Harga Termurah (JHT):
                    <h3>Jaminan Kompensasi Setinggi 2x Selisih Harga</h3>
                    <p>Pesan tiket pesawat, kamar hotel, dan aktivitas di destinasi domestik di Tiket.com dengan jaminan harganya lebih murah dari online travel platform lainnya.</p>
                    <p>Pengguna bisa mendapat kompensasi sebesar 2x selisih harga dalam bentuk tiket Points jika menemukan dan membuktikan bahwa produk serupa dihargai lebih murah di online travel platform lain.</p>
                    <h3>Proses Klaim Mudah</h3>
                    <p>Formulir klaim dapat diakses dari halaman Pemesanan. Masukkan URL harga yang ditemukan di online travel platform lain dan screenshot maksimal 12 jam setelah transaksi sebagai tanda bukti. Pengguna kemudian menunggu konfirmasi dari Tiket.com mengenai klaim terkait maksimal satu hari kerja.</p>
                    <p>Sementara itu, berikut top lima hal yang perlu diperhatikan pengguna yang hendak menggunakan fitur Jaminan Harga Termurah:</p>
                    <p>1. Jaminan Harga Termurah hanya berlaku untuk harga terakhir yang ditampilkan pada halaman pemesanan/pembayaran terakhir. Harga yang ditampilkan sudah termasuk pajak dan biaya-biaya lainnya. Kode promo, gift voucher, private rates, poin membership/loyalty points, hingga diskon keanggotaan, tidak akan diperhitungkan dalam JHT.</p>
                    <p>2. JHT hanya berlaku untuk harga yang ditampilkan oleh OTA lainnya. Oleh karena itu, selisih harga antara JHT Tiket.com dan travel agent konvensional (non-online platform) tidak diakui.</p>
                    <p>3. JHT hanya dapat diajukan dalam waktu 12 jam setelah e-voucher diterbitkan oleh Tiket.com. Klaim JHT yang diajukan setelah periode 12 jam akan otomatis ditolak.</p>
                    <p>4. JHT hanya berlaku untuk produk dalam negeri (penerbangan, akomodasi, aktivitas, atraksi, dll).</p>
                    <p>5. JHT berlaku untuk pesanan Flights, Hotels & Aktifitas (To Do, Atraksi, Event, Spa & Kecantikan, Playground, Olahraga & Outdoor, Tes COVID-19, Pelengkap Perjalanan) domestik di Tiket.com.</p>
                    <p>"Peluncuran JHT hadir di momentum yang tepat di saat masyarakat mulai melakukan perjalanan ke luar kota semenjak Pemerintah melonggarkan peraturan perjalanan, dan ini juga menandakan era travelling cermat bagi masyarakat," pungkas Maria.</p>
