                    <h1 class="judul">
                    7 Makanan untuk Meningkatkan Daya Ingat, Cegah Kebiasaan Lupa</h1>
                    <h3 class="penerbit">
                    CNN Indonesia | 6 Desember 2022 20:05                    
                    </h3>
                    <img src="../img-kesehatan/Berita 2.jpg" alt="abc" class="hero-img" width="600px">
                    <div class="isi-content">
                        <p>Jakarta, CNN Indonesia - Pola makan ternyata berpengaruh terhadap daya ingat seseorang. Buktinya, ada beberapa makanan untuk meningkatkan daya ingat.</p>
                        <p>"Ada diet khusus yang disarankan untuk kesehatan otak yang optimal. Kami memiliki bukti kuatnya," ujar ahli memori dan kesehatan otak, Babak Tousi, mengutip laman Cleveland Clinic.
                        </p>
                        <p>Beberapa makanan ini disebut dapat membantu meningkatkan kesehatan otak. Pasalnya, seiring pertambahan usia, otak akan mengalami perubahan.</p>
                        <p>Beberapa jenis diet telah terbukti memberikan manfaatnya untuk otak. Misalnya saja diet Mediterania, yang selain bermanfaat untuk kesehatan jantung, juga memberikan khasiatnya dengan meminimalisasi perubahan pada otak. "Ditemukan bahwa diet Mediterania juga menurunkan perubahan materi putih pada otak dan meningkatkan ketebalan di bagian materi abu-abu," jelas Tousi. Diet lain yang direkomendasikan untuk menjaga kesehatan otak adalah diet MND, kombinasi Mediterania dengan DASH. Diet ini fokus pada makanan yang meningkatkan kesehatan otak.
                        </p>
                        <p> Sebelum perilisan daftar obat sirup yang dilarang BPOM dari PT REMS ini, sebelumnya BPOM juga mencabut izin edar sejumlah perusahaan farmasi swasta karena terbukti menggunakan bahan baku kimiamelebih ambang batas aman. Perusahaan yang sudah menerima sanksi tersebut meliputi PT Yarindo Farmatama, PT Universal Pharmaceutical Industries dan PT Afi Farma, PT Samco Farma dan PT Subros Farma. Sehingga, saat ini total terdapat 204 daftar obat yang dilarang BPOM, termasuk 6 obat dari PT REMS.
                        </p>
                        <h3>Makanan untuk Meningkatkan Daya Ingat</h3>
                        <p>Untuk mengetahui lebih lanjut, berikut beberapa makanan untuk meningkatkan daya ingat yang bisa Anda konsumsi, mengutip Healthline.</p>
                        <h3>1. Ikan salmon</h3>
                        <p>Salmon dan ikan berlemak lainnya berada di urutan teratas dalam daftar makanan yang baik untuk kesehatan otak. Kandungan asam lemak omega-3 yang dimiliki salmon dibutuhkan otak untuk membangun sel-sel dan saraf. Asam lemak satu ini juga berperan penting untuk meningkatkan memori. Selain salmon, Anda juga bisa mendapatkan manfaat ini dari ikan kembung, tuna, dan sarden.
                        </p>
                        <h3>2. Blueberry</h3>
                        <p>Blueberry dan buah beri berwarna gelap lainnya menghasilkan anthocyanin, senyawa tanaman yang memiliki sifat anti-inflamasi dan antioksidan. Antioksidan bertindak dalam melawan stres oksidatif yang memicu peradangan, kondisi yang dapat menyebabkan penuaan otak. Berdasarkan salah satu ulasan dari 11 studi yang ada, blueberry disimpulkan dapat membantu meningkatkan daya ingat dan proses kognitif tertentu pada anak dan kelompok lanjut usia (lansia).
                        </p>
                        <h3>3. Kunyit</h3>
                        <p>Curcumin, senyawa aktif dalam kunyit, terbukti dapat melintasi penghalang antara darah dan otak. Artinya, darah dapat langsung masuk ke otak dan memberikan manfaatnya untuk sel-sel yang ada di sana.  Tak cuma itu, curcumin juga ditemukan dapat membantu meningkatkan daya ingat pada penderita Alzheimer.
                        </p>
                        <h3>4. Brokoli</h3>
                        <p>Brokoli merupakan salah satu sayuran yang kaya akan vitamin K. Vitamin satu ini dibutuhkan untuk menjaga kesehatan otak. Vitamin K berperan penting dalam membentuk sphingolipids. Nama terakhir merupakan sejenis lemak yang dikemas dengan padat ke dalam sel otak. Tak cuma itu, beberapa penelitian pada lansia juga telah mengaitkan hubungan antara vitamin K dengan kemampuan daya ingat dan status kognitif yang lebih baik. Di luar vitamin K, brokoli juga mengandung senyawa anti-inflamasi dan antioksidan yang dapat melindungi otak dari kerusakan.
                        </p>
                        <h3>5. Dark chocolate</h3>
                        <p>Dark chocolate kaya akan nutrisi yang dibutuhkan otak seperti flavonoid, kafein, dan senyawa antioksidan. Flavonoid, misalnya, yang dibutuhkan dalam proses pembelajaran dan daya ingat. Para peneliti percaya bahwa senyawa ini dapat meningkatkan daya ingat dan menekan risiko penurunan mental akibat pertambahan usia.
                        </p>
                        <h3>6. Kacang</h3>
                        <p>Tak cuma menjaga kesehatan jantung, kacang juga baik dikonsumsi demi otak yang sehat. Studi tahun 2014 menemukan bahwa wanita yang mengonsumsi kacang secara teratur memiliki ingatan yang lebih tajam dibandingkan mereka yang tidak mengemil kacang. Khasiat ini diduga diperoleh dari kandungan lemak sehat, antioksidan, dan vitamin E pada kacang. Semuanya memberikan manfaat baik untuk kesehatan otak.
                        </p>
                        <h3>7. Telur</h3>
                        <p>Telur kaya akan vitamin B6, B12, folat, dan kolin. Semuanya punya peran penting dalam menjaga kesehatan otak. Kolin merupakan mikronutrien penting yang digunakan tubuh untuk memproduksi asetilkolin. Nama terakhir merupakan neurotransmitter yang membantu mengatur memori. Penelitian bahkan menemukan bahwa asupan kolin yang tinggi dapat membantu meningkatkan daya ingat dan fungsi mental. Dengan demikian, telur menjadi salah satu makanan untuk meningkatkan daya ingat yang mudah didapatkan.
                        </p>
